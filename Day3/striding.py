# Striding: Striding in Python means skipping items by a set amount when you look at a list, text, or array.

a= 'data science'
print(a[::]) # data science
print(a[::1]) #data science
print(a[::2]) # dt cec 

c= "Machine Learning"
print(c[::5])
print(c[::7])
print(c[::2])
print(c[3:11])
print(c[:8])
print(c[9:])
print(c[::4])
print(c[::10])

d= "Cloud computing"
print(d[1:9:2])
print(d[2:13:3])
print(d[6:14:4])
print(d[5:12:2])
print(d[3:9:5])