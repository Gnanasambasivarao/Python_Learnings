# Data type means it indicates that what type of data is stored in variable 

# We use "type()" built-in functions to find the type of data
# Types of Data Types:

# 1. Integer = It indicates the numbers from 1 to 0
a=10
print(a,type(a))

# 2. Float= It is numbers with decimal points
b=10.56
print(b,type(b))

# 3. String = Collection of group of characters.
name="Sambasivarao"
print(name,type(name))

# complex = it iscombination of real number with imaginary numbers(j)
ac=5+6j
print(a, type(ac)) 

# Boolean = It indicates weather it is True or False
aT=True
af=False
print(aT,af, type(aT),type(af))
