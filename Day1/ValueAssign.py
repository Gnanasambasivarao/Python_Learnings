# The value can be assigned to the variable in different ways in python

a=10
b=20
print(a+b) #1 possible way

c,d=10,20
print(c+d) # second possible way

ad=10,20,30
print(ad) # third possible way 

# Different ways to provide space

first="Thota"
last="Samba"
print(first,last)
print(first+last)
print(first+" "+last)

